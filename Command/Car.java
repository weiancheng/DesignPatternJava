public class Car {
    public Car() {
    }

    public void forward() {
        System.out.println("car goes forward");
    }

    public void back() {
        System.out.println("car goes back");
    }

    public void right() {
        System.out.println("car turns right");
    }

    public void left() {
        System.out.println("car turns left");
    }
}