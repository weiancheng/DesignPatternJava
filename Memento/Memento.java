public class Memento {
    public String name;
    public int attack;
    public int defence;
    public int hp;
    public int mp;

    public Memento(String name, int attack, int defence, int hp, int mp) {
        this.name = name;
        this.attack = attack;
        this.defence = defence;
        this.hp = hp;
        this.mp = mp;
    }
}